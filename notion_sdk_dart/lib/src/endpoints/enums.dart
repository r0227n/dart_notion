enum RollupFunction implements EnumMethod {
  count,
  countValues,
  empty,
  notEmpty,
  unique,
  showUnique,
  percentEmpty,
  percentNotEmpty,
  sum,
  average,
  median,
  min,
  max,
  range,
  earliestDate,
  latestDate,
  dateRange,
  checked,
  unchecked,
  percentChecked,
  percentUnchecked,
  countPerGroup,
  percentPerGroup,
  showOriginal;

  @override
  String get snakeCase {
    return toString()
        .split('.')
        .last
        .replaceAllMapped(RegExp(r'[A-Z]'), (match) => '_${match.group(0)}')
        .toLowerCase();
  }
}

